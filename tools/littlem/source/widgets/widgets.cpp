{
	glRasterPos2f(0.0f, 12.0f);
	for(std::string::size_type stI = 0; stI < sString.length(); ++stI)
	{
		glBitmap(6, 12, 0, 0, 6, 0, g_ppuLetters[sString[stI] - CHARACTEROFFSET]);
	}
}

